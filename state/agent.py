"""
LangGraph 에이전트 공유 State 정의.

Meerkat Scanner(전략 수립)와 Owl Director(매매 의사결정) 두 에이전트가
공유하는 상태 객체다. 백테스트와 실거래 모드 모두에서 사용된다.
"""

from __future__ import annotations

from typing import Annotated, TypedDict

from langgraph.graph.message import add_messages


class AgentState(TypedDict):
    # ------------------------------------------------------------------ #
    # 공통 메시지 채널 (Meerkat ↔ Owl 대화 이력)
    # ------------------------------------------------------------------ #
    messages: Annotated[list, add_messages]

    # ------------------------------------------------------------------ #
    # 세션 메타
    # ------------------------------------------------------------------ #
    session_id: str
    """한 번의 실행 단위 ID (백테스트 세션 또는 실거래 세션)."""

    user_prompt: str
    """원본 사용자 요청. Meerkat이 전략 해시 생성에 사용."""

    # ------------------------------------------------------------------ #
    # 전략 설정 입력값
    # ------------------------------------------------------------------ #
    symbol: str
    """분석 대상 종목 심볼. 예: 'KRW-BTC'"""

    style: str
    """매매 스타일. 예: 'aggressive' | 'stable' | 'balanced'"""

    # ------------------------------------------------------------------ #
    # 전략 출력값 (Meerkat → Engine 전달)
    # ------------------------------------------------------------------ #
    strategy_id: str | None
    """Meerkat이 MongoDB에 저장한 전략 _id."""

    strategy: dict | None
    """Meerkat이 확정한 전략 전체 JSON. Owl이 의사결정에 사용."""

    # ------------------------------------------------------------------ #
    # 실행 루프 컨텍스트 (Engine → Owl 전달)
    # ------------------------------------------------------------------ #
    current_ohlcv_window: list[dict] | None
    """Owl에게 전달되는 현재 시점까지의 OHLCV 캔들 리스트 (최근 N개)."""

    current_timestamp: str | None
    """현재 시뮬레이션/실거래 시각 (ISO 8601 문자열)."""
